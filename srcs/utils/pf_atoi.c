#include "ft_printf.h"

ssize_t		pf_atoi(t_printf *f)
{
	ssize_t res;

	res = 0;
	while (*f->str && *f->str > 47 && *f->str < 58)
	{
		res *= 10;
		res += *f->str - 48;
		f->str++;
	}
	return (res);
}
