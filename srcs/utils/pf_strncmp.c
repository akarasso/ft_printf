#include "ft_printf.h"

int		pf_strncmp(const char *s1, char *s2, int n)
{
	while (*s1 && *s2 && *s1 == *s2 && n > 1)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
